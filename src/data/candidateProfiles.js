// Helper to find profile by name or ID
export function findCandidateProfile(nameOrId) {
  return CANDIDATE_PROFILES.find(
    p => p.id === nameOrId || p.name.toLowerCase() === nameOrId?.toLowerCase()
  );
}

// Helper to find profiles by party
export function getCandidatesByParty(party) {
  return CANDIDATE_PROFILES.filter(p => p.party === party);
}

// Helper to find profiles by tag
export function getCandidatesByTag(tag) {
  return CANDIDATE_PROFILES.filter(p => p.tags?.includes(tag));
}
